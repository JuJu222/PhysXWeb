<h3>Hukum Kepler</h3>
<p>Setelah adanya penemuan mengenai gravitasi yang ditemukan oleh Isaac Newton, seorang ahli matematika dan astronomi yang berasal dari Jerman yaitu Johannes Kepler berhasil menemukan 3 (tiga) hukum tentang pergerakan planet dalam tata surya, dimana Hukum-hukum Kepler ini sesuai dengan hukum gravitasi Newton. </p>
<br/>
<h4>Hukum 1 Kepler</h4>
<p>“Semua planet bergerak pada lintasan elips yang mengitari matahari, dimana matahari terletak pada salah satu titik pusatnya.”</p>
<p>Pada dasarnya hukum ini menjelaskan bahwa lintasan di tata surya berbentuk elips, dimana elips itu memiliki 2 titik fokus, dan matahari terletak pada salah satu titik fokusnya.</p>
<br/>
<h4>Hukum 2 Kepler</h4>
<p>“Luas daerah yang disapu oleh garis antara matahari dan planet adalah sama untuk setiap periode waktu yang sama.”</p>
<p>Pada dasarnya hukum ini menjelaskan bahwa karena lintasannya berbentuk elips, suatu planet tidak memiliki jarak pasti, yang ada adalah titik terjauh (titik aphelion), titik terdekat (titik perihelion), dan rata-rata jarak ke matahari. Dimana kecepatan orbit akan melambat pada saat berada aphelion, dan akan lebih cepat pada perihelion.</p>
<p class="fw-bolder">Kecepatan orbit maksimum suatu planet pada saat berada di aphelion</p>
