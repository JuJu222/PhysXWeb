</figure>
<p class="fw-bolder">Dalam gerak parabola, komponen sumbu x merupakan komponen GLB.</p>
<p>GLB merupakan kecepatan di sumbu horizontal pada titik ataupun posisi tetap.</p>
<br/>
<h3>Sumbu X</h3>
<p>Pada sumbu x, komponen awal ialah simbol dari kecepatan awal.</p>
