<h3>Sumbu Y</h3>
<p class="fw-bolder">Jika sumbu x merupakan komponen GLB, sumbu y atau arah vertikal komponen gerak merupakan GLBB.</p>
<p>Perbedaan sumbu x dengan sumbu y ialah simbol perpindahan/jarak pada sumbu x ditunjukkan dengan s, sedangkan pada sumbu y ditunjukkan dengan y.</p>
<p>Sumbu y kecepatan awal disimbolkan dengan. Sehingga, dapat dirumuskan sebagai berikut:</p>
