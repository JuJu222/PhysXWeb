<p>Gerak parabola adalah <span class="fw-bolder">gabungan antara gerak lurus beraturan (GLB) dan gerak lurus berubah beraturan (GLBB)</span>.</p>
<p class="fw-bolder">Gerak parabola merupakan gerak dua dimensi suatu benda yang bergerak membentuk sudut elevasi dengan sumbu x atau sumbu y. </p>
<p>Sumbu x (horizontal) merupakan GLB dan sumbu y (vertikal) merupakan GLBB.</p>
<p>Kedua gerak ini tidak saling memengaruhi, hanya saja membentuk suatu gerak parabola.</p>
<p>Berikut adalah contoh gerakan parabola.</p>
