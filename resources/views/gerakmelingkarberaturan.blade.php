<h4>Frekuensi Sudut (Kecepatan Sudut)</h4>
<p>Kenapa kecepatan tangensial pada GMB tetap? Hal ini disebabkan oleh kecepatan sudut yang juga tetap. Kecepatan sudut atau frekuensi sudut (Ï‰) adalah besarnya sudut yang ditempuh tiap detiknya.</p>
<p>Kecepatan sudut ini nilainya tetap karena arah kecepatan sudut sama dengan arah putaran benda.</p>
<p>Untuk menghitung nilai kecepatan sudut, kamu bisa menggunakan rumus berikut.</p>
