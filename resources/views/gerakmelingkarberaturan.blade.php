<p>Gerak Melingkar dibagi menjadi dua macam, yakni <span class="fw-bolder">Gerak Melingkar Beraturan (GMB) dan Gerak Melingkar Berubah Beraturan (GMBB)</span>.</p>
<p>Namun, kali ini kita akan fokus ke GMB terlebih dahulu.</p>
<p class="fw-bolder">Gerak Melingkar Beraturan (GMB) adalah gerak suatu benda yang menempuh lintasan melingkar dengan kecepatan atau kelajuan yang tetap.</p>
<p>GMB merupakan jenis gerak yang mirip, namun tidak sama dengan Gerak Lurus Beraturan (GLB). Perbedaannya terdapat pada jenis lintasannya. Pada GMB, lintasannya melingkar, sedangkan pada GLB, lintasannya lurus.</p>
<p>Contohnya seperti gerak jarum jam. Jarum jam itu bergerak melingkar dengan kecepatan yang tetap, nggak ngebut-ngebut apalagi berhenti mendadak, karena berjalannya waktu itu tetap alias sama saja dari hari ke hari. </p>
<p>Contoh lain dari GMB yaitu gerak yang terjadi pada bianglala dan komedi putar. Biasanya, bianglala dan komedi putar akan bergerak dengan kecepatan yang tetap, supaya antara 1 sesi dengan sesi lainnya, memakan waktu yang sama.</p>
<br/>
<h3>Besaran dan Rumus pada Gerak Melingkar Beraturan (GMB)</h3>
<p>Pada Gerak Melingkar Beraturan, terdapat beberapa besaran yang perlu kamu tahu. Mulai dari kecepatan tangensial, percepatan tangensial, kecepatan sudut, hingga percepatan sentripetal.</p>
<br/>
<h4>Kecepatan Tangensial dan Percepatan Tangensial</h4>
<p>Pada GMB, bendanya itu bergerak dengan kecepatan tangensial yang konstan (tetap) alias tidak berubah-ubah, sehingga percepatan tangensialnya bernilai 0 (nol).</p>
<p>Kecepatan tangensial (v) adalah kecepatan yang selalu menyinggung lintasan dan tegak lurus dengan jari-jari lintasan. Sedangkan percepatan tangensial adalah percepatan yang sejajar dengan lintasan.</p>
<p>Meskipun nilai kecepatan tangensialnya tetap, tetapi vektor (arah) dari kecepatan tangensialnya berubah setiap saat atau tidak tetap.</p>
