<h4>Percepatan Sentripetal</h4>
<p>Percepatan sentripetal (as) adalah percepatan yang tegak lurus dengan kecepatan tangensial, selalu mengarah ke pusat lintasan, dan hanya mengubah arah kecepatan (tidak dengan besarnya).</p>
<figure>
    <img src="https://www.ruangguru.com/hs-fs/hubfs/rumus%20percepatan%20sentripetal.jpg?width=600&name=rumus%20percepatan%20sentripetal.jpg" class="rounded mx-auto d-block">
    <figcaption class="text-center">Sumber: Ruang Guru | Ratu</figcaption>
</figure>
<p>Berdasarkan gambar di atas, dapat dilihat bahwa vektor (arah) percepatan sentripetal (as) selalu menuju ke pusat lingkaran.</p>
<p>Sedangkan vektor kecepatan tangensial (v) arahnya lurus. Sementara itu, vektor kecepatan sudut (Ï‰) searah dengan putaran benda. </p>
<p>Jadi, ketiganya memiliki arah yang berbeda-beda.</p>
<p>Selain itu, berdasarkan persamaan di atas, dapat disimpulkan bahwa semakin besar kecepatan tangensialnya, maka akan semakin besar pula percepatan sentripetalnya.</p>
<br/>
<h4>Periode dan Frekuensi</h4>
<p>Periode adalah waktu yang diperlukan untuk berputar satu putaran penuh.</p>
<p>Sedangkan frekuensi adalah banyaknya putaran yang ditempuh oleh suatu benda selama 1 detik. </p>
<p>Rumus periode dan frekuensi bisa kamu lihat pada gambar berikut.</p>
