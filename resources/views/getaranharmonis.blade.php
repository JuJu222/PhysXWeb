<p>Getaran Harmonis adalah sebuah benda yang bergerak secara bolak balik (periodik) melalui titik kesetimbangan. Grafik letak partikel ini diartikan sebagai fungsi waktu yang berupa sinus dinyatakan dalam bentuk sinus dan kosinus. Gerak ini juga sering dinamakan sebagai gerak osilasi.</p>
<p>Gambaran atau contoh dari sistem yang menggunakan prinsip getaran harmonis sendiri misalnya seperti, dawai pada alat musik, gelombang radio, arus listrik AC dan denyut jantung.</p>
<br/>
<h3>Syarat-syarat Getaran Harmonis</h3>
<p>1. Sistem Gerakannya secara periodik atau bolak-balik.</p>
<p>2. Proses Gerakannya akan selalu melewati kedudukan keseimbangan.</p>
<p>3. Kemudian pada Percepatan atau gaya yang bekerja yang terdapat pada sebuah benda akan dapat sebanding dengan kedudukan atau simpangan benda.</p>
<p>4. Kemudian Arah dalam percepatan atau gaya yang bekerja yang ada didalam suatu benda selalu mengarah kedudukan keseimbangan.</p>
<br/>
<p>Gerak harmonik sederhana memiliki simpangan, kecepatan, percepatan, dan energi.</p>
<br/>
<h3>Simpangan</h3>
<br/>
<p>Simpangan getaran harmonik sederhana dapat dianggap sebagai proyeksi partikel yang bergerak melingkar beraturan pada diameter lingkaran.</p>
