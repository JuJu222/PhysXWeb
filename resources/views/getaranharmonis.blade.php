</figure>
<p>Simpangan maksimum memiliki nilai yang sama dengan amplitudo (y = A), sehingga percepatan maksimumnya adalah am= – Aw</p>
<h3>Energi</h3>
<p>Persamaan energi pada gerak harmonik sederhana meliputi energi kinetik, energi potensial, dan energi mekanik.</p>
<p>Energi kinetik benda dapat dirumuskan sebagai berikut.</p>
