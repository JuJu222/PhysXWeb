<h3>Definisi Momentum</h3>
<p>Momentum adalah ukuran kesulitan untuk memberhentikan benda.</p>
<p>Semakin berat benda, maka momentum akan semakin besar.</p>
<p>Semakin cepat benda bergerak, maka momentum juga akan semakin besar.</p>
<br/>
<h3>Definisi Impuls</h3>
<p>Impuls adalah gaya yang diperlukan untuk membuat suatu benda menjadi bergerak.</p>
<p>Tentu ada interval waktu tertentu di sana.</p>
<br/>
<h3>Rumus Momentum dan Impuls</h3>
<p>Impuls dan momentum termasuk dalam besaran vektor, sehingga akan memiliki nilai dan arah.</p>
<p>Momentum memiliki arah yang sama (searah) dengan kecepatannya.</p>
<p>Arah impuls searah dengan gaya impulsifnya.</p>
<br/>
<h4>Rumus Momentum</h4>
<p>Dari uraian tentang momentum atau yang dilambangkan dengan p di atas, kamu tau kalau momentum dipengaruhi oleh massa (m) dan kecepatan (v), dengan masing-masing satuannya berturut-turut yaitu kg dan m/s. Secara matematis, berikut adalah rumus momentum:</p>
<div class="d-flex justify-content-center"><i class="fw-bolder display-4">p = m.v</i></div>
<p>p : momentum (kg.m/s)</p>
<p>m : massa (kg)</p>
<p>v : kecepatan (v)</p>
<p>Massa merupakan suatu besaran skalar, karena dia gak punya arah. Sedangkan, kecepatan merupakan besaran vektor yang punya arahnya. Nah, karena besaran skalar dan besaran vektor kalau disatukan akan menghasilkan besaran vektor, itulah mengapa momentum merupakan besaran vektor, yaitu besaran yang memiliki arah. Jadi, ketika kamu tau besaran momentum, berarti kamu juga harus tau arahnya kemana.</p>
<br/>
<h4>Rumus Impuls</h4>
<p>Impuls dilambangkan dengan huruf “I” dengan satuannya yaitu Ns. Berikut ini adalah rumus impuls:</p>
<div class="d-flex justify-content-center"><i class="fw-bolder display-4">I = F.Δt</i></div>
<p>I : impuls (Ns)</p>
<p>F : gaya impulsif (N)</p>
<p>Δt : perubahan waktu (s)</p>
<br/>
<h3>Hubungan Momentum dan Impuls</h3>
<br/>
<h5>Ingatkah kamu dengan Hukum II Newton berikut ini?</h5>
<div class="d-flex justify-content-center"><h4 class="text-center">“Gaya (F) yang diberikan pada suatu benda akan sama besarnya dengan perubahan momentum (Δp) per satuan waktu (Δt)”.</h4></div>
<p>Nah, kamu bisa melihat hubungan keduanya dari persamaan matematika sebagai berikut:</p>
<div class="d-flex justify-content-center"><i class="fw-bolder display-6">F = m.a (sesuai Hukum Newton II)</i></div>
<p>dimana,</p>
<div class="d-flex justify-content-center"><i class="fw-bolder display-6">a = Δv/Δt = (v2-v1) / Δt</i></div>
<p>sehingga,</p>

<div class="d-flex justify-content-center"><i class="fw-bolder display-6">F = m ((v2-v1) / Δt)</i></div>

<div class="d-flex justify-content-center"><i class="fw-bolder display-6">F.Δt = m.v2 – m.v1</i></div>

<div class="d-flex justify-content-center"><i class="fw-bolder display-6">F.Δt = p2 – p1</i></div>

<div class="d-flex justify-content-center"><i class="fw-bolder display-6">I = Δp</i></div>
<br/>
<p>Itu dia hubungan antara momentum dan impuls. Di mana, impuls sama dengan perubahan momentum yang dialami suatu benda.</p>
