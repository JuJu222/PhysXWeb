<p>GLBB merupakan gerak lurus suatu benda yang kecepatannya berubah karena adanya percepatan tetap. Maksud percepatan tetap ialah percepatan selalu sama terhadap waktu. Karena adanya percepatan, rumus jarak yang ditempuh tidak lagi linier, melainkan kuadratik.</p>
