<p>Gerak adalah <span class="fw-bolder">perubahan kedudukan suatu benda dari posisi awal</span>.</p>
<p>Benda dikatakan bergerak ketika benda mengalami perpindahan atau menempuh suatu jarak tertentu.</p>
<p>Berdasarkan lintasannya, gerak terbagi menjadi <span class="fw-bolder">3 jenis</span>, yaitu <span class="fw-bolder">gerak lurus, gerak melingkar, dan gerak melengkung (parabola).</span></p>
<p class="fw-bolder">Benda yang bergerak pada lintasan lurus disebut gerak lurus.</p>
<p>Gerak mengenal istilah jarak dan perpindahan.</p>
<p>Jarak adalah panjang lintasan yang ditempuh benda tanpa memperhatikan arahnya.</p>
<p>Sementara itu, perpindahan didefinisikan sebagai panjang lintasan, namun memperhatikan arah atau kedudukan awal dan akhir benda tersebut.</p>
