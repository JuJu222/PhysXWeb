<h3>Jenis-jenis Gerak Lurus</h3>
<p class="fw-bolder">Gerak lurus dibedakan menjadi 2, yaitu gerak lurus beraturan (GLB) dan gerak lurus berubah beraturan (GLBB).</p>
<p>GLB merupakan gerak benda pada lintasan lurus dengan kecepatan yang tetap atau tanpa percepatan.</p>
<figure>
    <img src="https://lh5.googleusercontent.com/JpjBhV2qCPq-I32ubOyVHcKOE_ymeVHCnNu1r5MSNp4c9PcTFEVqTpgqscM7sWiyDuafj18TwwP2C3ChRIrZdvykLy3uh60en1S9RpbnPSeQ5ugvl4pcqKmvl0g_lC4YyAHPb4egjEqGO69Rjg" class="rounded mx-auto d-block">
