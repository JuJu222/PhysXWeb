<p>HK II Newton berbunyi “Jika ada resultan gaya yang bekerja pada sebuah benda, maka akan dihasilkan suatu percepatan dalam arah yang sama dengan resultan gaya. Besarnya percepatan tersebut berbanding lurus terhadap resultan gaya dan berbanding terbalik terhadap massa bendanya”</p>
<br/>
<h3>Hukum 3 Newton</h3>
