<p>Hukum I Newton berbunyi “jika resultan gaya yang bekerja pada benda sama dengan nol maka benda yang mula-mula diam akan tetap diam dan benda yang mula-mula bergerak lurus beraturan akan tetap bergerak lurus beraturan”.</p>
<br/>
<h3>Hukum 2 Newton</h3>
<figure>
    <img src="https://www.ruangguru.com/hs-fs/hubfs/HK%20II%20Newton.jpg?width=600&name=HK%20II%20Newton.jpg" class="rounded mx-auto d-block">
