<h3>Usaha</h3>
<p class="fw-bolder">Usaha adalah besarnya energi untuk merubah posisi yang diberikan gaya pada benda atau objek. </p>
<p>Usaha yang dilakukan suatu objek didefinisikan sebagai perkalian antara jarak yang ditempuh dengan gaya yang searah dengan perpindahannya.</p>
<p>Usaha dinotasikan dengan W yang merupakan singkatan bahasa Inggris dari Work yang berarti kerja.</p>
<p>Satuan usaha adalah Joule yang didefinisikan sebagai besarnya energi yang dibutuhkan untuk memberi gaya sebesar satu Newton sejauh satu meter. </p>
<p>Oleh sebab itu, 1 Joule sama dengan 1 Newton meter (N.m).</p>
<br/>
<p>Rumus Usaha dinotasikan dengan:</p>
<figure>
    <img src="https://s0.wp.com/latex.php?latex=W+%3D+F+%5Ccdot+x&bg=f9f9f9&fg=000000&s=0&c=20201002" class="rounded mx-auto d-block">
</figure>
<p>W = Usaha yang dilakukan (Joule)</p>
<p>F = Gaya yang diberikan (N)</p>
<p>x = jarak perpindahan objek (m)</p>
<br/>
