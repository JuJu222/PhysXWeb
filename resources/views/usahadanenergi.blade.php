<h4>Energi Potensial</h4>
<p>Energi Potensial adalah energi yang dimiliki benda karena posisinya atau bentuk maupun susunannya.</p>
<p>Energi Potensial disebabkan adanya gaya gravitasi. Suatu benda memiliki energi potensial yang besar jika massanya semakin besar dan ketinggiannya semakin tinggi.</p>
