    <figcaption class="text-center">Sumber: Studio Belajar</figcaption>
</figure>
<img src="https://s0.wp.com/latex.php?latex=%5Ctheta&bg=f9f9f9&fg=000000&s=0&c=20201002"><span> = sudut yang dibentuk gaya terhadap perpindahan.</span>
<p>Nilai usaha dapat berupa positif atau negatif tergantung arah gaya terhadap perpindahannya. </p>
<p>Jika gaya yang diberikan pada objek berlawanan arah dengan perpindahannya, maka usaha yang diberikan bernilai negatif.</p>
<p>Jika gaya yang diberikan searah dengan perpindahan, maka objek tersebut melakukan usaha positif.</p>
<br/>
<p>Usaha juga dapat bernilai nol (0) atau objek tidak melakukan usaha jika,</p>
<p>1. Diberikan gaya namun tidak terjadi perpindahan.</p>
<p>2. Gaya yang diberikan tegak lurus dengan perpindahan (<span><img src="https://s0.wp.com/latex.php?latex=%5Ccos+90%5E%7B%5Ccirc%7D%3D0&bg=f9f9f9&fg=000000&s=0&c=20201002"></span>)</p>
